class Solution {
  public:
    vector<int> kLargest(vector<int>& nums, int k) {
        // Sort the vector in descending order
        sort(nums.begin(), nums.end(), greater<int>());
        
        // Return the first 'k' largest elements
        vector<int> result(nums.begin(), nums.begin() + k);
        return result;
    }
};

